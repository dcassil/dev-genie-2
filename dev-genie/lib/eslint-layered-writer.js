'use strict';

// Layered eslint config writer.
//
// Emits `eslint.config.guardrails.mjs` at the repo root that imports the user's
// existing eslint config and re-exports `[...userConfig, { rules: {...} }]`.
// When the user's config is legacy `.eslintrc*`, we cannot cleanly layer via
// flat-config import; the caller should fall back to the managed-block writer.
//
// Optional `rewriteEntryPoint`: rewrite the user's eslint config so it imports
// the layered file. A backup is written alongside.

const fs = require('node:fs');
const path = require('node:path');

const FLAT_NAMES = ['eslint.config.mjs', 'eslint.config.js', 'eslint.config.cjs', 'eslint.config.ts'];
const LEGACY_NAMES = ['.eslintrc', '.eslintrc.json', '.eslintrc.js', '.eslintrc.cjs', '.eslintrc.yaml', '.eslintrc.yml'];

function exists(p) { try { fs.accessSync(p); return true; } catch { return false; } }

function findEslintEntry(repo) {
  for (const n of FLAT_NAMES) {
    const p = path.join(repo, n);
    if (exists(p)) return { kind: 'flat', file: n, full: p };
  }
  for (const n of LEGACY_NAMES) {
    const p = path.join(repo, n);
    if (exists(p)) return { kind: 'legacy', file: n, full: p };
  }
  return null;
}

function renderRulesObject(rules) {
  const entries = Object.entries(rules || {});
  if (entries.length === 0) return '{}';
  const lines = entries.map(([name, value]) => {
    return `    ${JSON.stringify(name)}: ${JSON.stringify(value)},`;
  });
  return `{\n${lines.join('\n')}\n  }`;
}

function buildLayeredSource(userEntryRel, rules) {
  // Use a relative import. For .ts the user must run with a TS-aware loader;
  // we still emit the import — eslint v9 supports both.
  const importPath = './' + userEntryRel.replace(/\\/g, '/');
  return `// Generated by dev-genie. Do not edit by hand.
// Layers dev-genie's enforced rules on top of the user's existing eslint config.
// Re-run \`/dev-genie-init\` to regenerate.

import userConfig from ${JSON.stringify(importPath)};

const guardrailsLayer = {
  rules: ${renderRulesObject(rules)},
};

export default [
  ...(Array.isArray(userConfig) ? userConfig : [userConfig]),
  guardrailsLayer,
];
`;
}

/**
 * Write `eslint.config.guardrails.mjs` at repo root that layers `rules` on top
 * of the user's existing eslint flat config.
 *
 * @param {string} repoPath
 * @param {Record<string, unknown>} rules eslint rule overrides
 * @param {object} [opts]
 * @param {boolean} [opts.rewriteEntryPoint=false] rewrite user's config to
 *   re-export the layered file (with backup).
 * @returns {{ ok: boolean, mode: 'flat'|'fallback-legacy'|'no-config', path?: string, fallbackReason?: string, rewroteEntryPoint?: boolean }}
 */
function writeLayeredEslintConfig(repoPath, rules, opts = {}) {
  const repo = path.resolve(repoPath);
  const entry = findEslintEntry(repo);
  if (!entry) return { ok: false, mode: 'no-config', fallbackReason: 'no eslint config detected' };
  if (entry.kind === 'legacy') {
    return { ok: false, mode: 'fallback-legacy', fallbackReason: `legacy ${entry.file} detected; use managed-block writer instead` };
  }

  const layeredFile = path.join(repo, 'eslint.config.guardrails.mjs');
  const src = buildLayeredSource(entry.file, rules);
  fs.writeFileSync(layeredFile, src, 'utf8');

  let rewrote = false;
  if (opts.rewriteEntryPoint) {
    const backupFile = entry.full + '.dev-genie.bak';
    if (!exists(backupFile)) fs.copyFileSync(entry.full, backupFile);
    const proxy = `// Replaced by dev-genie. Original preserved at ${path.basename(backupFile)}.
export { default } from './eslint.config.guardrails.mjs';
`;
    fs.writeFileSync(entry.full, proxy, 'utf8');
    rewrote = true;
  }

  return { ok: true, mode: 'flat', path: layeredFile, rewroteEntryPoint: rewrote };
}

module.exports = { writeLayeredEslintConfig, buildLayeredSource, findEslintEntry };
